package model;

public class Campus {
    int cID;
    String cName;

    public Campus() {
    }

    public Campus(int cID, String cName) {
        this.cID = cID;
        this.cName = cName;
    }

    public int getcID() {
        return cID;
    }

    public void setcID(int cID) {
        this.cID = cID;
    }

    public String getcName() {
        return cName;
    }

    public void setcName(String cName) {
        this.cName = cName;
    }
    
}
